function AccountDetails({ navigation }) {
  const authContext = useContext(AuthContext);

  const handleLogOut = () => {
    Firebase.auth()
      .signOut()
      .then(() => {
        // Sign-out successful.
        authContext.setUser(null);
      })
      .catch((error) => {
        console.log(error);
      });
  };
  useFocusEffect(
    React.useCallback(() => {
      const reloadUser = async () => {
        try {
          await auth.currentUser.reload(); // refresh Firebase user
          authContext.setUser(auth.currentUser); // update context
        } catch (err) {
          console.log("USER RELOAD ERROR:", err);
        }
      };

      reloadUser();
    }, [])
  );
  
  return (
    <Screen style={styles.screen}>
      <View style={styles.container}>
        <FlatList
          data={menuItems}
          keyExtractor={(item) => item.title}
          ItemSeparatorComponent={ListItemSeparator}
          renderItem={({ item }) => (
            <ListItem
              title={item.title}
              onPress={() => navigation.navigate(item.target)}
              IconComponent={
                <Icon
                  name={item.icon.name}
                  backgroundColor={item.icon.backgroundColor}
                />
              }
            />
          )}
			    scrollEnabled={false} // needed to prevent list of options from scrolling
        />
      </View>
    </Screen>
  );
}

